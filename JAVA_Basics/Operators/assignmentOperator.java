package JAVA_Basics.Operators;

public class assignmentOperator {
    public static void main(String[] args) {
        int a = 10;
        a += 10;

        int b = 20;
        b -= 10;

        int c = 50;
        c *= 10;

        int d = 60;
        d/= 10;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
