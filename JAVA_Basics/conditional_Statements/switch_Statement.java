package JAVA_Basics.conditional_Statements;

public class switch_Statement {
    public static void main(String[] args) {
        int num = 2;
        switch(num){
            case 1 : System.out.println("Himanshu 1");
            break;
            case 2 : System.out.println("Himanshu 2");
            break;
            case 3 : System.out.println("Himanshu 3");
            break;
            default : System.out.println();
        }
    }
}
